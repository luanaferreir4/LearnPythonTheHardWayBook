# + : soma 
# - : subtração
# * : multiplicação
# / : divisão
# % : resto
# < : menor que 
# > : maior que 
# <= : menor ou igual a
# >= : maior ou igual a

print("Agora vou contar meus frangos:")

# O "," dá o espaço.
print("Galinhas", 25 + 30 / 6)  # Galinhas 30.0
print("Galos", 100 - 25 * 3 % 4)  # Galos 97

print("Agora vou contar ovos:")

print(3 + 2 + 1 - 5 + 4 % 2 - 1 / 4 + 6) # 6.75 *Seguindo o Operator Precedence = https://www.faceprep.in/python/python-operator-precedence-associativity/

print("É verdade que 3 + 2 < 5 - 7?")
print(3 + 2 < 5 - 7)   # False 

print("Quanto é 3 + 2?", 3 + 2)  # 5
print("Quanto é 5 - 7?", 5 - 7)   # -2

print("Por isso que é falso!")

print("Vamos testar mais um pouco")

print("É maior?", 5 > -2)  # True
print("É maior ou igual?", 5 >= -2)  # True
print("É menor ou igual?", 5 <= -2)  # False 

# As operações funcionam de acordo com as regras de "Operator Precedence" da linguagem.

